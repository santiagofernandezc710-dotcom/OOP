public class Numero 
{
    //Campos o Atributos.
    private int numero;

    //Constructores.
    public Numero() 
    {
        
    }

    public Numero(int numero) 
    {
        this.numero=numero;
    }

    //Metodos.
    public int getNumero() 
    {
        return numero;
    }

    public void setNumero(int numero) 
    {
        this.numero=numero;
    }   
}
